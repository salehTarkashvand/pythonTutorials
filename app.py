# tuple

point = (1, 2, 3)

print(point)  # (1, 2, 3)

point = (1, 2, 3) + (3, 4, 5)

print(point)  # (1, 2, 3, 3, 4, 5)

point = (1, 2, 3) * 4

print(point)  # (1, 2, 3, 1, 2, 3, 1, 2, 3, 1, 2, 3)

point = (1, 2, 3)

print(point[0])  #1 type="int"

point = (1, 2, 3)

print(point[0:2])  #(1, 2) type="tuple"
