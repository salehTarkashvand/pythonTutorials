# formatted string

name = "saleh"
lastName = "tarkashvand"

# This line of code is using a formatted string in Python. It is creating a new string variable `full`
# by combining the values of the `name` and `lastName` variables with a space in between. The `f`
# before the string indicates that it is a formatted string, allowing you to embed expressions inside
# curly braces `{}` within the string.

full = f"{name} {lastName}"
